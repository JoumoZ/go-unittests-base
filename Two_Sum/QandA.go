package Two_Sum

//Q: https://leetcode.com/explore/interview/card/top-interview-questions-easy/92/array/546/

func TwoSum(nums []int, target int) []int {
	for i := 0; i < len(nums)-1; i++ {
		for j := i + 1; j < len(nums); j++ {
			if nums[i]+nums[j] == target {
				return []int{i, j}
			}
		}
	}
	return []int{}
}
